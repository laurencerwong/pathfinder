#ifndef cocos2d_normal_Buttons_h
#define cocos2d_normal_Buttons_h

typedef enum Button : NSInteger{
    MoveButton              =   1 << 0,
    CombatMoveButton        =   1 << 1,
    UseEquipmentButton      =   1 << 2,
    ChangeEquipmentButton   =   1 << 3,
    UndoButton              =   1 << 4,
    EndTurnButton           =   1 << 5,
    
    ToggleableButtons       =   MoveButton | CombatMoveButton | UseEquipmentButton |ChangeEquipmentButton,
    NontoggleableButtons    =   UndoButton | EndTurnButton
    
} Button;

#endif
